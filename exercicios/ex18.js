// 18) Faça um programa que leia um número entre 0 e 10, e escreva este número por extenso. Use o comando
// switch. Crie um case default que escreva ‘Número fora do intervalo.’

function numeroExtenso(numero) {
    switch (numero) {
        case 0:
            console.log("o numero por extenso é zero")
            break
        case 1:
            console.log("o numero por extenso é um")
            break
        case 2:
            console.log("o numero por extenso é dois")
            break
        case 3:
            console.log("o numero por extenso é tres")
            break
        case 4:
            console.log("o numero por extenso é quatro")
            break
        case 5:
            console.log("o numero por extenso é cinco")
            break
        case 6:
            console.log("o numero por extenso é seis")
            break
        case 7:
            console.log("o numero por extenso é sete")
            break
        case 8:
            console.log("o numero por extenso é oito")
            break
        case 9:
            console.log("o numero por extenso é nove")
            break
        case 10:
            console.log("o numero por extenso é dez")
            break
        default:
            console.log("numero fora do intervalo")
    }
}

